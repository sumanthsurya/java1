public class Whileloops5 {
    public static void main(String[] args) {
        int i = 1;
        int fact = 1;
        System.out.println("The factorial of 24 :-");
        while (i <= 5) {
            fact = fact * i;
            System.out.println(fact);
            i++;

        }
        System.out.println(fact);

    }
}

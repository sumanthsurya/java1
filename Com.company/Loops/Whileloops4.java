public class Whileloops4 {
    public static void main(String[] args) {
        int a = 1;
        // int b = 0;

        System.out.println("The multiplicant of 4&5 tables");
        while (a <= 10) {
            System.out.println("4*" + a + "=" + (a * 4) + "     " + "5*" + a + "=" + (a * 5));

            a = a + 1;
        }

    }
}
